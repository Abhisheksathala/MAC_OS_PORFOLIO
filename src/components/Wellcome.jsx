import React from 'react';
import gsap from 'gsap';
import { useGSAP } from '@gsap/react';

const FONT_WEIGHTS = {
  subtitle: {
    min: 100,
    max: 400,
    default: 100,
  },
  title: {
    min: 400,
    max: 900,
    default: 400,
  },
};

const renderText = (text, className, baseWeight = 400) =>
  [...text].map((char, i) => (
    <span key={i} className={className} style={{ fontVariationSettings: `'wght' ${baseWeight}` }}>
      {char === ' ' ? '\u00A0' : char}
    </span>
  ));

const setupTextHover = (container, type) => {
  if (!container) return;

  const letters = container.querySelectorAll('span');
  const { min, max, default: base } = FONT_WEIGHTS[type];

  const animateLetter = (letter, weight, duration = 0.25) =>
    gsap.to(letter, {
      duration,
      ease: 'power2.out',
      fontVariationSettings: `'wght' ${weight}`,
    });

  const handleMouseMove = (e) => {
    const { left } = container.getBoundingClientRect();
    const mouseX = e.clientX - left;

    letters.forEach((letter) => {
      const { left: l, width: w } = letter.getBoundingClientRect();
      const center = l - left + w / 2;
      const distance = Math.abs(mouseX - center);
      const intensity = Math.exp(-(distance ** 2) / 20000);

      animateLetter(letter, min + (max - min) * intensity);
    });
  };

  const handleMouseLeave = () => {
    letters.forEach((letter) => animateLetter(letter, base, 0.3));
  };

  container.addEventListener('mousemove', handleMouseMove);
  container.addEventListener('mouseleave', handleMouseLeave);
};

const Wellcome = () => {
  const titleRef = React.useRef(null);
  const subtitleRef = React.useRef(null);

  useGSAP(() => {
    const titlecleanup = setupTextHover(titleRef.current, 'title');
    const subtitlecleanup = setupTextHover(subtitleRef.current, 'subtitle');

    return () => {
      titlecleanup, subtitlecleanup;
    };
  }, []);

  return (
    <section id="welcome">
      <p ref={subtitleRef}>
        {renderText("Where code meets motion.", 'text-3xl font-georama', 100)}
      </p>

      <h1 ref={titleRef}>{renderText('Abhishek', 'text-9xl  italic font-georama', 400)}</h1>

      <div className="small-screen">
        <p>This Portfolio is designed for desktop/tablet screen only.</p>
      </div>
    </section>
  );
};

export default Wellcome;
